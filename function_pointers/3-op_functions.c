/**
 * op_add - Sum of two ints
 * @a: Num1
 * @b: Num2
 * Return: Result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - difference of two ints
 * @a: Num1
 * @b: Num2
 * Return: Result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Product of two ints
 * @a: Num1
 * @b: Num2
 * Return: Result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division of two ints
 * @a: Num1
 * @b: Num2
 * Return: Result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Remainder of the division between two ints
 * @a: Num1
 * @b: Num2
 * Return: Result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
